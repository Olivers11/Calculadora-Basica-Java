    public  String getResultado()
    {
        n2 = Double.parseDouble(jTextField1.getText());
        if("+".equals(Operador))
        {
            double val1 = n1+n2;
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            return Double.toString(val2);
        }
        else if("-".equals(Operador))
        {
            double val1 = n1-n2;
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            return Double.toString(val2);
        }
        else if("/".equals(Operador))
        {
            double val1 = n1/n2;
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            return Double.toString(val2);
        }
        else
        {
            double val1 = n1*n2;
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            return Double.toString(val2);
        }
    }
    
    public void PintarOperacion(String operacion){
        if("cos".equals(operacion))
        {
            jLabel1.setText("cos(" + Double.toString(n1)+")");
        }
        else if("sen".equals(operacion))
        {
            jLabel1.setText("sen(" + Double.toString(n1)+")");
        }
        else if("tan".equals(operacion))
        {
            jLabel1.setText("tan(" + Double.toString(n1)+")");
        }
        else if("r".equals(operacion))
        {
            jLabel1.setText("sqrt(" + Double.toString(n1)+")");
        }
        else if("log".equals(operacion))
        {
            jLabel1.setText("log(" + Double.toString(n1)+")");
        }
        else
        {
            jLabel1.setText(Double.toString(n1)+" " +operacion+" ");
        }
        
    }
    
    public double Factorial(double n){
        if(n == 0 || n == 1){
            return n;
        }
        else
        {
            return n * Factorial(n-1);
        }
    }
    
