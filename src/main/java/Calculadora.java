        if("!".equals(Operador))
        {
            double val1 = Factorial(n1);
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            jTextField1.setText(Double.toString(val2));
        }
        else if("cos".equals(Operador))
        {
            double val1 = Math.toRadians(n1);
            val1 = Math.cos(val1);
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            jTextField1.setText(Double.toString(val2));
        }
        else if("sen".equals(Operador))
        {
            double val1 = Math.toRadians(n1);
            val1 = Math.sin(val1);
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            jTextField1.setText(Double.toString(val2));
        }
        else if("tan".equals(Operador))
        {
            double val1 = Math.toRadians(n1);
            val1 = Math.tan(val1);
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            jTextField1.setText(Double.toString(val2));
        }
        else if("r".equals(Operador))
        {
            double val1 = Math.sqrt(n1);
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            jTextField1.setText(Double.toString(val2));
        }
        else if("log".equals(Operador))
        {
            double val1 = Math.log10(n1);
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            jTextField1.setText(Double.toString(val2));
        }
        else if("^3".equals(Operador))
        {
            double val1 = Math.pow(n1, 3);
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            jTextField1.setText(Double.toString(val2));
        }
        else if("^2".equals(Operador))
        {
            double val1 = Math.pow(n1, 2);
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            jTextField1.setText(Double.toString(val2));
        }
        else if("^".equals(Operador))
        {
            n2 = Double.parseDouble(jTextField1.getText());
            double val1 = Math.pow(n1, n2);
            BigDecimal bd = new BigDecimal(val1).setScale(2, RoundingMode.HALF_UP);
            double val2 = bd.doubleValue();
            jTextField1.setText(Double.toString(val2));
            jLabel1.setText(jLabel1.getText()+ Double.toString(n2)  );
        }
        else
        {
            n2 = Double.parseDouble(jTextField1.getText());
            jTextField1.setText(getResultado());
            jLabel1.setText(jLabel1.getText()+ Double.toString(n2)  );
        }
        
        
